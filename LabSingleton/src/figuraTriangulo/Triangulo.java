public class Triangulo {

	/**
	 * Metodo que cria o triangulo passando como parametro os seus lados
	 * 
	 * @param lado1
	 * @param lado2
	 * @param lado3
	 * @return String
	 */
	public String criarTriangulo(double lado1, double lado2, double lado3) {
		return "Triangulo criado com lados: " + lado1 + ", " + lado2 + ", " + lado3 + "";
	}
}
